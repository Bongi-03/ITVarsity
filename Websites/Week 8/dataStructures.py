#Lists
#Lists are ordered, mutable collections in Python.
#Created with square brackets []
#Example:
fruits = ["apple", "banana", "orange"]
#You can:
#Access elements by index
#Modify elements
#Add with .append() / .insert()
#Remove with .remove() / .pop()
#Loop through with for loops

#Tuples
#Tuples are ordered, immutable collections.
#Created with parentheses ()
#Example:
dimensions = (1920, 1080)
#Can access elements by index but cannot change their values after creation.

#Sets
#Sets are unordered collections of unique elements.
#Created with curly braces {} or set() function.
#Example:
colors = {"red", "blue", "green"}
#No duplicates allowed.
#Can perform set operations: union, intersection, difference, etc.

#Dictionaries
#Dictionaries are key-value pairs.
#Created with curly braces {}.
#Example:
person = {"name": "Alice", "age": 30}
#You can:
#Access, add, and modify values by key
#Loop through keys, values, or both using .items(), .keys(), .values()
#Remove items with del or .pop()
    